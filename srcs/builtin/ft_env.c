#include "builtin.h"

int	ft_env(char **envv)
{
	int	i;

	i = 0;
	while (envv[i])
	{
		write(0, envv[i], ft_strlen(envv[i]));
		write(0, "\n", 1);
		i++;
	}
	return (0);
}
